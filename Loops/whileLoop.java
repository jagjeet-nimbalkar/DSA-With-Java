package Loops;

public class whileLoop {
    public static void main(String[] args) {
        int age=10;
        while(age<15){
            age++;
            System.out.println(age+ " " +"Monkey D Luffy");
        }    

        int no = 0;
        while (no<=10) {
            System.out.println(no);
            no++;
        }
    }
}
